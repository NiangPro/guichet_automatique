    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        var code= numCpt.getSelectedItem();
        
        
        if(code== null){           
            JOptionPane.showMessageDialog(null, "Vous n'avait pas de compte pour le moment", "Recherche", JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                ResultSet rs = db.getCompteByNumCpt(code.toString());
                if(rs.next()){
                    r_compte.setText(rs.getString("numCpt"));
                    r_solde.setText(rs.getFloat("solde")+" $");
                    r_type.setText(rs.getString("type"));
                   
                }
                resultat.setVisible(true);
            } catch (SQLException ex) {
                Logger.getLogger(ClientDashboard.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

