    private void getComptes(){
        ResultSet rs = db.getCompteByCode(client.getCode());
        try {
            while(rs.next()){
                numCpt.addItem(rs.getString("numCpt"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormDepot.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
